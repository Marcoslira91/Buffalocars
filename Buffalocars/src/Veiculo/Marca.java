package Veiculo;

public class Marca {

	// Atributos
	private String descricao;

	// Contrutores
	public Marca(String _descricao) {
		this.descricao = _descricao;
	}

	// Metodos
	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
